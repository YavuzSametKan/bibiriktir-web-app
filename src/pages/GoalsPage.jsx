import React, { useState, useEffect } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import { toast } from 'react-toastify';
import { PlusIcon } from '@heroicons/react/24/outline';
import AddGoalModal from '../components/goals/AddGoalModal';
import CelebrateConfetti from '../components/goals/CelebrateConfetti';
import GoalCard from '../components/goals/GoalCard';
import AddContributionModal from '../components/goals/AddContributionModal';
import GoalDetailModal from '../components/goals/GoalDetailModal';
import goalsService from '../services/goalsService';

const GoalsPage = () => {
  const [goals, setGoals] = useState([]);
  const [isAddGoalModalOpen, setIsAddGoalModalOpen] = useState(false);
  const [showConfetti, setShowConfetti] = useState(false);
  const [filter, setFilter] = useState('active'); // 'all', 'active', 'completed'
  const [selectedGoal, setSelectedGoal] = useState(null);
  const [isAddContributionModalOpen, setIsAddContributionModalOpen] = useState(false);
  const [isDetailModalOpen, setIsDetailModalOpen] = useState(false);
  const [loading, setLoading] = useState(true);

  // Hedefleri yÃ¼kle
  useEffect(() => {
    const fetchGoals = async () => {
      try {
        const response = await goalsService.getGoals();
        if (response.success) {
          setGoals(response.data.goals);
        }
      } catch (error) {
        toast.error(error.message || 'Hedefler yÃ¼klenirken bir hata oluÅŸtu');
      } finally {
        setLoading(false);
      }
    };

    fetchGoals();
  }, []);

  const handleAddGoal = async (newGoal) => {
    try {
      const response = await goalsService.createGoal(newGoal);
      if (response.success) {
        setGoals(prevGoals => [...prevGoals, response.data]);
        toast.success('Yeni hedef baÅŸarÄ±yla oluÅŸturuldu.');
        setIsAddGoalModalOpen(false);
      }
    } catch (error) {
      toast.error(error.message || 'Hedef oluÅŸturulurken bir hata oluÅŸtu');
    }
  };

  const handleAddContribution = async (contribution) => {
    console.log('Selected Goal:', selectedGoal);

    if (!selectedGoal || !selectedGoal._id) {
      console.error('Hedef seÃ§ilmedi veya geÃ§ersiz hedef:', selectedGoal);
      toast.error('Hedef seÃ§ilmedi');
      return;
    }

    try {
      console.log('KatkÄ± ekleniyor:', {
        goalId: selectedGoal._id,
        contribution
      });

      const response = await goalsService.addContribution(selectedGoal._id, contribution);
      console.log('KatkÄ± ekleme yanÄ±tÄ±:', response);
      
      if (!response.success) {
        toast.error(response.message || 'KatkÄ± eklenirken bir hata oluÅŸtu');
        return;
      }

      const updatedGoal = await goalsService.getGoalDetails(selectedGoal._id);
      console.log('GÃ¼ncellenmiÅŸ hedef:', updatedGoal);
      
      if (!updatedGoal.success) {
        toast.error('Hedef gÃ¼ncellenirken bir hata oluÅŸtu');
        return;
      }

      setGoals(prevGoals => {
        return prevGoals.map(goal => {
          if (goal._id === selectedGoal._id) {
            return updatedGoal.data;
          }
          return goal;
        });
      });

      const progress = (updatedGoal.data.currentAmount / updatedGoal.data.targetAmount) * 100;
      if (progress >= 80 && progress < 100) {
        toast.info('Hedefine Ã§ok az kaldÄ±, harika gidiyorsun!');
      } else if (progress >= 100) {
        toast.success('ðŸŽ‰ Hedefine ulaÅŸtÄ±n!');
        setShowConfetti(true);
        setTimeout(() => setShowConfetti(false), 5000);
      }

      toast.success('KatkÄ± baÅŸarÄ±yla kaydedildi.');
      setIsAddContributionModalOpen(false);
      setSelectedGoal(null);

    } catch (error) {
      console.error('KatkÄ± ekleme hatasÄ±:', error);
      toast.error(error.message || 'KatkÄ± eklenirken bir hata oluÅŸtu');
    }
  };

  const handleUpdateContribution = async (goalId, contributionId, updatedContribution) => {
    try {
      console.log('GÃ¼ncellenecek katkÄ±:', { goalId, contributionId, updatedContribution });
      const response = await goalsService.updateContribution(goalId, contributionId, updatedContribution);
      console.log('KatkÄ± gÃ¼ncelleme yanÄ±tÄ±:', response);

      if (response.success) {
        const updatedGoal = await goalsService.getGoalDetails(goalId);
        if (updatedGoal.success) {
          setGoals(prevGoals => prevGoals.map(goal => 
            goal._id === goalId ? updatedGoal.data : goal
          ));
          if (selectedGoal && selectedGoal._id === goalId) {
            setSelectedGoal(updatedGoal.data);
          }
          toast.success('KatkÄ± baÅŸarÄ±yla gÃ¼ncellendi.');
        } else {
          toast.error('Hedef gÃ¼ncellenirken bir hata oluÅŸtu.');
        }
      } else {
        toast.error(response.message || 'KatkÄ± gÃ¼ncellenirken bir hata oluÅŸtu.');
      }
    } catch (error) {
      console.error('KatkÄ± gÃ¼ncelleme hatasÄ±:', error);
      toast.error('KatkÄ± gÃ¼ncellenirken bir hata oluÅŸtu.');
    }
  };

  const handleDeleteContribution = async (goalId, contributionId) => {
    try {
      console.log('Silinecek katkÄ±:', { goalId, contributionId });
      const response = await goalsService.deleteContribution(goalId, contributionId);
      console.log('KatkÄ± silme yanÄ±tÄ±:', response);

      if (response.success) {
        const updatedGoal = await goalsService.getGoalDetails(goalId);
        if (updatedGoal.success) {
          setGoals(prevGoals => prevGoals.map(goal => 
            goal._id === goalId ? updatedGoal.data : goal
          ));
          if (selectedGoal && selectedGoal._id === goalId) {
            setSelectedGoal(updatedGoal.data);
          }
          toast.success('KatkÄ± baÅŸarÄ±yla silindi.');
        } else {
          toast.error('Hedef gÃ¼ncellenirken bir hata oluÅŸtu.');
        }
      } else {
        toast.error(response.message || 'KatkÄ± silinirken bir hata oluÅŸtu.');
      }
    } catch (error) {
      console.error('KatkÄ± silme hatasÄ±:', error);
      toast.error('KatkÄ± silinirken bir hata oluÅŸtu.');
    }
  };

  const handleUpdateGoal = async (updatedGoal) => {
    try {
      const response = await goalsService.updateGoal(updatedGoal._id, updatedGoal);
      if (response.success) {
        setGoals(prevGoals => prevGoals.map(goal => 
          goal._id === updatedGoal._id ? response.data : goal
        ));

        if (selectedGoal && selectedGoal._id === updatedGoal._id) {
          setSelectedGoal(response.data);
        }

        toast.success('Hedef baÅŸarÄ±yla gÃ¼ncellendi.');
      }
    } catch (error) {
      toast.error(error.message || 'Hedef gÃ¼ncellenirken bir hata oluÅŸtu');
    }
  };

  const handleDeleteGoal = async (goalId) => {
    try {
      const response = await goalsService.deleteGoal(goalId);
      if (response.success) {
        setGoals(prevGoals => prevGoals.filter(goal => goal._id !== goalId));
        toast.success('Hedef baÅŸarÄ±yla silindi.');
      }
    } catch (error) {
      toast.error(error.message || 'Hedef silinirken bir hata oluÅŸtu');
    }
  };

  const filteredGoals = goals
    .filter(goal => {
      const progress = (goal.currentAmount / goal.targetAmount) * 100;
      if (filter === 'active') return progress < 100;
      if (filter === 'completed') return progress >= 100;
      return true;
    })
    .sort((a, b) => {
      // createdAt alanÄ±na gÃ¶re sÄ±ralama (en yeniden en eskiye)
      return new Date(b.createdAt) - new Date(a.createdAt);
    });

  if (loading) {
    return (
      <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-8">
        <div className="flex justify-center items-center h-64">
          <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-indigo-600"></div>
        </div>
      </div>
    );
  }

  return (
    <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-8">
      <div className="flex justify-between items-center mb-8">
        <h1 className="text-2xl font-bold text-gray-900">Hedeflerim</h1>
        <button
          onClick={() => setIsAddGoalModalOpen(true)}
          className="inline-flex items-center px-4 py-2 border border-transparent rounded-lg shadow-sm text-sm font-medium text-white bg-indigo-600 hover:bg-indigo-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500"
        >
          <PlusIcon className="h-5 w-5 mr-2" />
          Yeni Hedef
        </button>
      </div>

      <div className="flex space-x-4 mb-6">
        <button
          onClick={() => setFilter('all')}
          className={`px-4 py-2 rounded-lg text-sm font-medium ${
            filter === 'all'
              ? 'bg-indigo-600 text-white'
              : 'bg-white text-gray-700 border border-gray-300 hover:bg-gray-50'
          }`}
        >
          TÃ¼mÃ¼
        </button>
        <button
          onClick={() => setFilter('active')}
          className={`px-4 py-2 rounded-lg text-sm font-medium ${
            filter === 'active'
              ? 'bg-indigo-600 text-white'
              : 'bg-white text-gray-700 border border-gray-300 hover:bg-gray-50'
          }`}
        >
          Aktif
        </button>
        <button
          onClick={() => setFilter('completed')}
          className={`px-4 py-2 rounded-lg text-sm font-medium ${
            filter === 'completed'
              ? 'bg-indigo-600 text-white'
              : 'bg-white text-gray-700 border border-gray-300 hover:bg-gray-50'
          }`}
        >
          Tamamlanan
        </button>
      </div>

      <AnimatePresence>
        <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
          {filteredGoals.map((goal, index) => (
            <GoalCard
              key={goal?._id ? `goal-${goal._id}` : `goal-${index}`}
              goal={goal}
              onAddContribution={() => {
                console.log('Setting selected goal:', goal);
                setSelectedGoal(goal);
                setIsAddContributionModalOpen(true);
              }}
              onViewDetails={() => {
                setSelectedGoal(goal);
                setIsDetailModalOpen(true);
              }}
            />
          ))}
        </div>
      </AnimatePresence>

      {filteredGoals.length === 0 && (
        <div className="text-center py-12">
          <p className="text-gray-500">HenÃ¼z hedef bulunmuyor.</p>
        </div>
      )}

      <AddGoalModal
        isOpen={isAddGoalModalOpen}
        onClose={() => setIsAddGoalModalOpen(false)}
        onAdd={handleAddGoal}
      />

      <AddContributionModal
        isOpen={isAddContributionModalOpen}
        onClose={() => {
          setIsAddContributionModalOpen(false);
          setSelectedGoal(null);
        }}
        onAdd={handleAddContribution}
        goal={selectedGoal}
      />

      <GoalDetailModal
        isOpen={isDetailModalOpen}
        onClose={() => setIsDetailModalOpen(false)}
        goal={selectedGoal}
        onUpdateContribution={handleUpdateContribution}
        onDeleteContribution={handleDeleteContribution}
        onUpdateGoal={handleUpdateGoal}
        onDeleteGoal={handleDeleteGoal}
      />

      {showConfetti && <CelebrateConfetti />}
    </div>
  );
};

export default GoalsPage; 